#include "JoystickDriver.c"

float map(float rLower, float rUpper, float dLower, float dUpper, float dVal)
{
	return (dVal - dLower) * (rUpper - rLower) / (dUpper - dLower)+rLower;
}

task main()
{
	bool togglePlate = false;
	bool toggleClamp = false;
	bool plateOpen = false;
	bool clampDown = false;
	servo[liftR] = 255;
	servo[liftL] = 5;
	servo[clampL] = 255;
	servo[clampR] = 0;
	while(true)
	{
		getJoystickSettings(joystick);
		if(joy1Btn(01))
		{
			if(!togglePlate)
			{
				if(plateOpen)
				{
					servo[clampL] = 255;
					servo[clampR] = 0;
					servo[liftR] = 255;
					servo[liftL] = 5;
				}
				else
				{
					servo[liftR] = 50;
					servo[liftL] = 220;
					servo[clampL] = 0;
					servo[clampR] = 255;
				}
				plateopen = !plateopen;
			}
			togglePlate = true;
		}
		else
		{
			togglePlate = false;
		}
		if (joy1Btn(03)||joy1Btn(02))
		{
			if(!toggleClamp)
			{
				if(clampDown)
				{
					servo[clampL] = 0;
					servo[clampR] = 255;
				}
				else
				{
					servo[clampL] = 255;
					servo[clampR] = 0;
				}
				clampDown = !clampDown;
			}
			toggleClamp = true;
		}
		else
		{
			toggleClamp = false;
		}
		if (joy1Btn(02))
		{
			if(clampDown)
			{
				wait10Msec(100);
			}
			servo[pushR] = 175;
			servo[pushL] = 75;
		}
		else
		{
			servo[pushR] = 5;
			servo[pushL] = 235;
		}
	}
}

